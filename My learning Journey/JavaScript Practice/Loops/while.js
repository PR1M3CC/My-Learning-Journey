while (index <= 10) {
    console.log(`The value of index is ${index}`);
    index = index + 1
}
// using while loop on an array:
let myArr = ["Ihsan", "Usman", "Umar", "Kamran"]
let i = 0
while (i < myArr.length) {
    console.log(myArr[i]);
    i = i + 1
}
